% imdct.m
% inverse modified DCT 
% N inputs, 2N outputs
%---------------------------------------------------------------

%---------------------------------------------------------------
function [y] = imdct(X)

    X = X(:);
    N = length(X);

    y = zeros(2*N,1);
    for n = 0:2*N-1 % M-1
        s = 0;
        for k = 0:N-1
            s = s + X(k+1)*cos( (pi/N)*(n + 1/2 + N/2)*(k + 1/2) );
        end
        y(n+1) = s/N;
    end
end
